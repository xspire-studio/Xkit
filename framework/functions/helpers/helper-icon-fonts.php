/**
 * Remove prefix icons
 *
 * @return array
 */
function xkit_remove_prefix_icons( $prefix = true, $icons ){ 
	if( !$prefix ){
		foreach( $icons as $icon ){
			$new = preg_replace( '/^.*?\s/ui', '', $icon );

			$icons_new[$new] = $new;
		}

		$icons = $icons_new;
	}

	return $icons;
}


